import UIKit

extension UIColor {
    
    public class var ftGreyDark: UIColor {
        return UIColor(red: 64.0/255.0, green: 64.0/255.0, blue: 64.0/255.0, alpha: 1.0)
    }
    
    public class var ftGreyAccent: UIColor {
        return UIColor(red: 98.0/255.0, green: 98.0/255.0, blue: 98.0/255.0, alpha: 1.0)
    }
    
    public class var ftGreyLight: UIColor {
        return UIColor(red: 196.0/255.0, green: 196.0/255.0, blue: 196.0/255.0, alpha: 1.0)
    }
    
    public class var ftPinkLight: UIColor {
        return UIColor(red: 242.0/255.0, green: 53.0/255.0, blue: 123.0/255.0, alpha: 1.0)
    }
}
